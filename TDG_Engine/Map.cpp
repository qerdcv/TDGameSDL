Map::Map() {
    surfaceMap[0] = Game::scp(TextureManager::LoadTexture("assets/water.png"));
    surfaceMap[1] = Game::scp(TextureManager::LoadTexture("assets/grass.png"));
    surfaceMap[2] = Game::scp(TextureManager::LoadTexture("assets/dirt.png"));
    
    LoadMap(lvl1);
    
    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;
    
    dest.x = dest.y = 0;
}

Map::~Map() = default;

void Map::LoadMap(int arr[20][25]) {
    for (int row = 0; row < 20; row++) {
        for (int col = 0; col < 25; col++) {
            map[row][col] = arr[row][col];
        }
    }
}


void Map::Render() {
    int type = 0;

    for (int row = 0; row < 20; row++) {
        for (int col = 0; col < 25; col++) {
            dest.x = col * dest.w;
            dest.y = row * dest.h;

            TextureManager::Draw(surfaceMap[map[row][col]], src, dest);
        }
    }
}
